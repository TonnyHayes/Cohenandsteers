function initDropDowns() {
	var isTouchDevice = ('ontouchstart' in window) || window.DocumentTouch && document instanceof DocumentTouch || /Windows Phone/.test(navigator.userAgent);
	var hoverClass = 'hover';
	var doc = jQuery(document);
	var animSpeed = 300;

	jQuery('.nav>.drop>ul').each(function() {
		var holder = jQuery(this);
		var items = holder.find('>li');

		items.each(function() {
			var item = jQuery(this);
			var link = item.find('>a.sub-opener');
			var drop = item.find('>.dropdown').css({display: 'block'}).hide();
			var buttonBack = drop.find('>.btn-back');
			var dropHeight = null;


			function slideDrop(open) {
				if(open) {
					drop.show();
					dropHeight = drop.find('>ul').innerHeight();
					drop.css({height: 0});
				} else {
					dropHeight = 0;
				}

				drop.stop().animate({
					height: dropHeight
				}, {
					duration: animSpeed,
					complete: function() {
						drop.css({
							height: ''
						})
						if(!open) drop.hide();
					}
				});
			}

			function closeAllDrops(mobiles) {
				if(!mobiles) return;
				var allDrops = items.filter('.hover').find('>.dropdown');
				items.removeClass(hoverClass);
				allDrops.stop().animate({
					height: 0
				},{
					duration: animSpeed,
					complete: function() {
						allDrops.hide();
					}
				});
			}

			if(isTouchDevice) {
				buttonBack.on('click touchstart', function(e) {
					e.preventDefault();
					item.removeClass(hoverClass);
				});
				link.on('click touchstart', function(e) {
					dropHeight = null;
					if(!item.hasClass(hoverClass)) {
						e.preventDefault();
						closeAllDrops(true);
						items.removeClass(hoverClass);

						item.addClass(hoverClass);
						slideDrop(true);
					}
				});

				function clickHandler(state) {
					doc.on('click touchstart', function(e) {
						var target = jQuery(e.target);
						if(!target.closest(holder).length) {
							closeAllDrops(state);
							items.removeClass(hoverClass);
						}
					});
				}

				ResponsiveHelper.addRange({
					'960..': {
						on: function() {
							clickHandler(true);
						},
						off: function() {
							clickHandler(false);
						}
					},
					'..959': {
						on: function() {
							clickHandler(false);
						},
						off: function() {
							clickHandler(true);
						}
					}
				});
				return;
			}

			item.on('mouseenter', function() {
				item.addClass(hoverClass);
				slideDrop(true);
			}).on('mouseleave', function() {
				item.removeClass(hoverClass);
				slideDrop();
			});
		});
	});
}

